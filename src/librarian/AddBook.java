    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:

        String id=jLabel7.getText();
        String name=jTextField1.getText();
        String category=(String)jComboBox1.getSelectedItem();
        String price=jTextField2.getText();
        String desc=jTextArea1.getText();

        try
        {
            FileInputStream fin = new FileInputStream(file.getAbsolutePath());

            Connection con=dbconnection.DbConnectivity.getConnect();
            PreparedStatement ps=con.prepareStatement("insert into books values(?,?,?,?,?,?)");
            ps.setString(1, id);
            ps.setString(2, name);
            ps.setString(3, category);
            ps.setString(4, price);
            ps.setString(5, desc);
            ps.setBinaryStream(6, fin, fin.available());
            int i=ps.executeUpdate();
            if(i>0)
            {
                JOptionPane.showMessageDialog(rootPane, "Book added successfully");
                AddBook ai=new AddBook(lb);
                ai.setVisible(true);
                setVisible(false);
            }
            else
            {
                JOptionPane.showMessageDialog(rootPane, "failed","Error",JOptionPane.ERROR_MESSAGE);
                LibrarianProfile ap=new LibrarianProfile(lb);
                ap.setVisible(true);
                setVisible(false);
            }
        }
        catch(Exception e)
        {
            System.out.println(e);
        }

    }//GEN-LAST:event_jButton2ActionPerformed

