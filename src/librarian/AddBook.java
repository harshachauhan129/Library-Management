    private void jLabel8MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel8MouseClicked
        // TODO add your handling code here:

        JFileChooser jf=new JFileChooser();
        jf.showOpenDialog(this);
        file=jf.getSelectedFile();

        URL url;
        try
        {
            url=file.toURI().toURL();
            ImageIcon icon=new ImageIcon(url);
            Image img=icon.getImage().getScaledInstance(120, 130, Image.SCALE_DEFAULT);
            ImageIcon icon1=new ImageIcon(img);
            jLabel9.setIcon(icon1);
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(this, "Please select an image");
        }

    }//GEN-LAST:event_jLabel8MouseClicked

    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        
        int rowcount=1;
        try
        {
            Connection con=dbconnection.DbConnectivity.getConnect();
            PreparedStatement ps=con.prepareStatement("select * from books");
            ResultSet rs=ps.executeQuery();
            while(rs.next())
            {
                rowcount=rowcount+1;
            }
            jLabel7.setText(String.valueOf("lm/"+rowcount));
        }
        catch(Exception e)
        {
            System.out.println(e);
        }
        
    }//GEN-LAST:event_formWindowOpened

